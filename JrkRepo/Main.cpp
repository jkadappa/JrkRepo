#include <iostream>


void add(int a,int b)
{
	std::cout<<"the sum of two numbers"<<a+b<<std::endl; 	
}
void sub(int a,int b)
{
	std::cout<<"the sum of two numbers"<<a-b<<std::endl; 
}
void mul(int a,int b)
{
	std::cout<<"the sum of two numbers"<<a*b<<std::endl; 
}



int main(int argc, char * argv[])
{
add(2,22);
  sub(2,24);
  mul(2,2);

add(3,5);
mul(8,8);
        
}